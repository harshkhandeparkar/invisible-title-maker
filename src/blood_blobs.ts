function randomDisplacement(min: number, max: number): number {
	return Math.round(
		min + Math.random() * (max - min)
	) * Math.sign(Math.random() - 0.5)
}

export interface IBlobInfo {
	path: string;
	translate: [number, number];
	scale: number;
	rotate: number;
}
export function generateBlobs(nBlobs: number): IBlobInfo[] {
	const blobs: IBlobInfo[] = [];

	for (let i = 0; i < nBlobs; i++) {
		const blobType = Math.min(Math.max(Math.round(Math.random() * (BLOB_PATHS.length + 1)), 0), BLOB_PATHS.length - 1);

		blobs.push({
			path: BLOB_PATHS[blobType],
			scale: Math.random() * 1.5,
			translate: [randomDisplacement(300, 550), randomDisplacement(150, 350)],
			rotate: Math.round(Math.random() * 180)
		})
	}

	return blobs;
}